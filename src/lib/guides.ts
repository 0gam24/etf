  paragraphs: string[];
  /** 동적 데이터 블록 키 — 페이지에서 컴포넌트로 처리 */
  dataBlock?:
    | 'income-stability-table'   // 안정성 등급 표
    | 'income-monthly-calendar'  // 월별 캘린더
    | 'income-tax-compare'       // 계좌별 세후 비교
    | 'income-yield-ranking'     // 분배율 랭킹
